public class JavaInfo {
    String copyright;
    String date;
    String explanation;
    String hdurl;
    String media_type;
    String service_version;
    String title;
    String url;

    // getter/setters
    public String getCopyright(){ return copyright; }
    public void setCopyright(String value) { copyright = value; }
    public String getDate(){ return date; }
    public void setDate(String value) { date = value; }
    public String getExplanation(){ return explanation; }
    public void setExplanation(String value) { explanation = value; }
    public String getHdurl(){ return hdurl; }
    public void setHdurl(String value) { hdurl = value; }
    public String getMedia_type(){ return media_type; }
    public void setMedia_type(String value) { media_type = value; }
    public String getService_version(){ return service_version; }
    public void setService_version(String value) { service_version = value; }
    public String getTitle(){ return title; }
    public void setTitle(String value) { title = value; }
    public String getUrl(){ return url; }
    public void setUrl(String value) { url = value; }
}
